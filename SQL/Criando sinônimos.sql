
-- CRIANDO SINONIMOS PRIVADOS

CREATE SYNONYM departamentos
FOR departments;

CREATE SYNONYM dept
FOR departments;

-- UTILIZANDO SINONIMOS

SELECT *
FROM departamentos;

SELECT *
FROM dept;

-- REMOVENDO SINONIMOS

DROP SYNONYM departamentos;

DROP SYNONYM dept;

-- CRIANDO SINONIMOS PUBLICOS PARA TABELAS EM OUTRO SCHEMA


